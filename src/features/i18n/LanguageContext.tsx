type LanguageContextValue = {
  language: AppLanguage
  setLanguage(language: AppLanguage): void
  t: Translate
}

const LanguageContext = createContext<LanguageContextValue | null>(null)

export function normalizeAppLanguage(value: unknown, fallback: AppLanguage = 'zh-CN'): AppLanguage {
  return value === 'en' || value === 'zh-CN' ? value : fallback
}

export function getStoredAppLanguage(): AppLanguage {
  try {
    return normalizeAppLanguage(localStorage.getItem(APP_LANGUAGE_STORAGE_KEY))
  } catch {
    return 'zh-CN'
  }
}

export function translateMessage(
  language: AppLanguage,
  key: MessageKey,
  params: MessageParams = {},
): string {
  const template = language === 'en' ? enMessages[key] : zhCNMessages[key]
  return template.replace(/\{(\w+)\}/g, (match, name: string) => String(params[name] ?? match))
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<AppLanguage>(getStoredAppLanguage)

  useLayoutEffect(() => {
    document.documentElement.lang = language
    try {
      localStorage.setItem(APP_LANGUAGE_STORAGE_KEY, language)
    } catch {
      // Keep the language active for this session if storage is unavailable.
    }
  }, [language])

  const value = useMemo<LanguageContextValue>(() => ({
    language,
    setLanguage,
    t: (key, params) => translateMessage(language, key, params),
  }), [language])

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>
}

export function useI18n() {
  const value = useContext(LanguageContext)
  if (!value) throw new Error('useI18n must be used inside LanguageProvider')
  return value
}
